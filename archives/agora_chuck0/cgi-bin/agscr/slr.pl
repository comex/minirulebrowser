Rule 1951/1 (Power=1)
Papyri

      Papyri (singular Papyrus) are a Bank Currency. The MUQ of
      Papyri is 0.1. The Recordkeepor for Papyri is the Promotor.

----------------------------------------------------------------------

Rule 1954/2 (Power=1)
Distribution of Papyri

      As soon as possible after the fifteenth day of each month, the
      Promotor shall Auction a number of Papyri. The Promotor must
      always auction at least one Papyrus. Otherwise, the Promotor may
      auction as many Papyri as e wishes.

      The items to be auctioned are individual Papyri, and thus
      the number of items is equal to the number of Papyri to be
      Auctioned. The Auctioneer shall be the Promotor, and the Auction
      shall be conducted in Stems. Only Scribes may bid.

      Upon the satisfaction of a debt arising from a Papyrus Auction,
      the Promotor shall pay out one Papyrus to the debtor of the
      satisfied debt.

      The Promotor may, within a week after the end of a Papyrus
      Auction, pay out to emself a number of Stems equal to the
      Winning Bid of the Auction, if there were at least three
      different winners in the Auction, not counting the Promotor
      emself.

