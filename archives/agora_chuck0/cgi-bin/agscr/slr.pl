Rule 1020/0 (Power=3)
Name Is Agora

      The Official Name of this Nomic shall be Agora.

----------------------------------------------------------------------

Rule 101/1 (Power=3)
Obey the Rules

      All Players must always abide by all the Rules currently in
      effect, in the form in which they are currently in effect.
      However, a Player besides the Speaker may always deregister
      rather than continue to play.

      Whatever is not prohibited or regulated by the Rules is
      permitted and unregulated, with the sole exception of changing
      the Rules, which is permitted only when the Rules explicitly or
      implicitly permit it.  Any change to the game state which would
      make it impossible to make arbitrary modifications to the Rules
      by any combination of actions by Players does not occur, any
      Rule to the contrary notwithstanding.

      The Rules in the Initial Set are in effect at the beginning of
      the first game.  The Initial Set consists of Rules 101-116
      (Immutable) and 201-219 (Mutable).

----------------------------------------------------------------------

Rule 1011/4 (Power=2)
Definition of Nomic Property

      A Nomic Property is any property of any entity the value of
      which is defined by the Rules.  Other Rules may define
      procedures by which the value of a Nomic Property may be
      changed.

----------------------------------------------------------------------

Rule 1527/2 (Power=1)
Timing of Multiple Events in One Message

      Whenever a message contains more than one notification, report,
      or other communication in which the Rules place some legal
      significance, the communications in that message shall be taken
      to have been sent sequentially, separated by infinitesimal
      increments of time, in the order which they appear in the
      message.

----------------------------------------------------------------------

Rule 754/4 (Power=1)
Spelling, Quoting, and Terminology

      Differences in spelling, grammar, or dialect, or the
      substitution of a word or phrase by a synonym or abbreviation,
      are inconsequential in all forms of Nomic communication, as long
      as there is no ambiguity in meaning.  A Player shall not be
      penalised for accurately quoting a Rule, Proposal, Statement,
      Judgement, the words of another Player, or other reference.

      Except when the Rules explicitly state otherwise, any
      mathematical term in the Rules shall be construed to have its
      standard mathematical meaning.  In particular, &quot;number&quot; shall
      mean &quot;real number&quot;.

      This Rule takes precedence over any other Rule which specifies
      terminology or grammar.

----------------------------------------------------------------------

Rule 1497/5 (Power=1)
Truth in Advertising

      A Player who presents as correct information which e believes to
      be incorrect as part of any message sent to the Public Forum or
      in response to a request for information which that Player is
      required to provide commits the Class 4 Crime of
      Misrepresentation.

      A Player who presents as correct information which e believes to
      be incorrect as part of a message to an Officer which that
      Officer is then required to act upon commits the Class 4 Crime
      of Falsification.

      A Player who presents as correct information which e believes to
      be incorrect as part of evidence in a Claim of Error, Call for
      Judgement, or Judgement commits the Class 10 Crime of Perjury.

      A Player shall never be convicted of more than one of the above
      Crimes for the same act.

----------------------------------------------------------------------

Rule 1926/1 (Power=1)
The Official Secrets Act

      The Rules may designate certain pieces of information to
      be secret. Any player who knowingly reveals secret information
      to another player, except as explicitly permitted or required
      by the rules, commits the Crime of Unlawful Disclosure, a Class
      5 Crime. All secret information is deemed private, with respect
      to the Freedom of Information Act.

----------------------------------------------------------------------

Rule 1478/4 (Power=1)
Executors

      The Executor of an entity is a Player who is empowered by the
      Rules to act on behalf of that entity, as if e were that entity.

      Except when the Rules stipulate otherwise, a Player is eir own
      Executor.  Any other entity has an Executor only when the Rules
      specify either a Player or a means to designate a Player to be
      the Executor of that entity.

      Whenever a Player is acting on behalf of some entity of which e
      is the Executor other than emself, e must make it clear that e
      is doing so.  If such a Player fails to clearly indicate that e
      is acting as the Executor of some other entity, it shall be
      presumed that e is acting on eir own behalf.

      Only a person or an entity with a Player Executor may perform
      actions.

----------------------------------------------------------------------

Rule 1630/3 (Power=1)
Impersonation

      Any Player who knowingly attempts to act as, or purports to be,
      the Executor of an Entity, of which e is not the Executor,
      commits the Class 4 Crime of Impersonation.

