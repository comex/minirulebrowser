Rule 1036/14 (Power=1)
The Proposal Pool

      As soon as possible after the Promotor receives a new Proposal,
      e shall place this Proposal in the Proposal Pool.

      A Proposal's Distribution Cost is an integer multiple of the MUQ
      of Papyri. The base Distribution Cost of a Proposal is 1, but
      this may be altered by other Rules.

