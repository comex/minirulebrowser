Rule 1467/7 (Power=1)
Definition of a Currency

      A Currency is a category of entities established by the Rules.
      Each instance of a Currency is a Property. Instances of a given
      Currency are fungible. The size of a single instance, or
      &quot;unit&quot;, of a given Currency is its Minimum Unit Quantity (MUQ).
      Individual units of a Currency cannot be divided; therefore,
      the final result of all computations involving numbers of units
      of Currency shall be rounded off to the nearest integral
      multiple of its MUQ.

      Unless otherwise specified, the MUQ of a Currency is one (1).

      Since units of Currency are fungible, it is not necessary for
      the Recordkeepor of a Currency to track the individual
      ownership of each unit; rather, it is sufficient to maintain a
      record of the total number of units possessed by each entity
      which possesses any number of units of that Currency.

----------------------------------------------------------------------

Rule 1579/6 (Power=1)
Mintors

      Each Currency shall have associated with it a Mintor, which must
      be an entity to which the Rules grant Mint Authority.  An entity
      has Mint Authority if and only if granted it by the Rules.

      If the Mintor of a Currency ceases to exist, or ceases to have
      the authority to be a Mintor, then all units of that Currency
      are destroyed, and that Currency ceases to exist.  The
      Recordkeepor of that Currency shall publicly announce when
      this happens.

      All Players have Mint Authority.

----------------------------------------------------------------------

Rule 1722/1 (Power=1)
Creation of New Currencies

      A new Currency is created when an entity which has Mint
      Authority posts to the Public Forum a message stating that e is
      doing so, provided that also e states in that message the values
      of all properties which the Rules require of Currencies.  The
      Mintor of a Currency created in this way is the entity which
      created it, any specification to the contrary notwithstanding.
      If the Recordkeepor of the Currency to be created is not the
      Executor of the Mintor, the proposed Recordkeepor must first
      consent to being its Recordkeepor before the Currency is
      created.

      A Currency is destroyed when the Mintor of that Currency posts
      in the Public Forum stating that e is doing so, unless this
      destruction would be prohibited by the Rules.

      The Mintor of a Currency may not destroy that Currency if that
      Currency exists by virtue of being required to exist by the
      Rules.

      The Recordkeepor of a Currency created by the procedure in this
      Rule is changed when the Mintor of that Currency announces the
      change; but if the new Recordkeepor is not the Executor of the
      Mintor, the new Recordkeepor must first consent to being made
      Recordkeepor.

----------------------------------------------------------------------

Rule 1974/0 (Power=1)
Restriction on Minting of Bank Currencies by the Bank

      The Bank, by the Treasuror, may Mint new units of a Bank
      Currency only as follows:

      (a) Once during the thirty day period immediately following the
          registration of a new Player, a number of Stems not to
          exceed five times the Basic Officer Salary per such new
          Player, with Two Supporters;

      (b) In the event that the Voting Entitlement Surplus exceeds the
          Bank's holdings of VEs, a number of VEs not to exceed the
          difference between the Voting Entitlement Surplus and the
          Bank's Holdings of VEs, with Two Supporters;

      (c) At any time, Without Objection.

      The Bank may not otherwise Mint new units of a Bank Currency
      except through the mechanism set forth in this Rule.

