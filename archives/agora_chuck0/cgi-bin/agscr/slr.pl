Rule 1952/0 (Power=1)
Making Proposals Distributable

      Each Proposal is either Distributable or Undistributable.
      Initially, each Proposal is Undistributable. When a Proposal
      becomes Disinterested, it immediately becomes Distributable
      and may not become Undistributable by any means as long as
      it remains Disinterested.

      Any Player may make any Undistributable Proposal in the Proposal
      Pool Distributable by publicly announcing that e is doing so and
      Paying a Fee of a number of Papyri equal to the amount of the
      Distribution Cost of the Proposal. Any Player may make any
      Interested Distributable Proposal in the Proposal Pool
      Undistributable by publicly announcing that e is doing so and
      Paying a Fee of a number of Papyri double the amount of the
      Distribution Cost of the Proposal.

----------------------------------------------------------------------

Rule 1770/14 (Power=1)
Distributing Proposals

      During each Week, the Promotor must either distribute every
      Proposal that was Distributable at the beginning of that Week,
      or announce that there were no such Proposals. Failure to do so
      is the Class 1 Infraction of Promotor Tardiness, which any
      Player may detect and report.

      The distribution of each Proposal shall be accompanied by the
      identity of its Proposing Entity, and an indication of whether
      the Proposal is Ordinary or Democratic. However, the failure of
      the Promotor to distribute any of these accompaniments with a
      Proposal does not deprive the distribution of the Proposal of
      any legal effect.

      A Proposal is legally distributed only if it is explicitly
      marked as such. When a Proposal is distributed, it is removed
      from the Proposal Pool.

      The Promotor shall not distribute any Proposal which is not
      Distributable, and must abort any Proposal so distributed. If
      the Promotor aborts a Proposal which e knows was Distributable
      at the time it was distributed, e commits the Class 2 Crime of
      Illegal Abortion.

      The Promotor aborts a Proposal by publishing an announcement to
      that effect, clearly identifying the Proposal which has been
      aborted.  When a Proposal is aborted:

      (a)  the Voting Period for that Proposal immediately ends, if it
           has not ended already;
      (b)  all Votes cast on that Proposal are cancelled, and the
           Proposal fails;
      (c)  the Assessor is relieved of any duty to report on Votes
           cast on that Proposal; and
      (d)  the Proposal is added to the Proposal Pool.

      The distribution of an Undistributable Proposal, when the
      Promotor knows that the Proposal is Undistributable, is the
      Class 2 Crime of Promotor Misrepresentation. The distribution of
      a text purporting to be a Proposal, when the Promotor knows that
      the text is not a Proposal, is the Class 5 Crime of Promotor
      Fraud.

