Rule 1728/10 (Power=2)
Dependent Actions

      An action is Dependent (or may be performed Dependently) if
      and only if it is an Action With N Objections or an Action
      With N Supporters, where N is a positive integer not greater
      than five.  The phrase `Without Objection' is synonymous with
      `Without 1 Objection', and the phrase `With Support' is
      synonymous with `With 1 Supporter'.  The Rules may name
      specific actions as Dependent Actions.

      A Player may, in a Public Forum, unambiguously describe a
      particular Dependent Action which e is authorised by the
      Rules to perform Dependently, and announce eir intent to
      perform it.  That Player may perform that action if and only if:

      (a)  e announced eir intent at most fourteen days before
            attempting to perform it, and, in the case of Actions
            Without N Objections, at least four days before attempting
            to perform it;

      (b)  the authorising Rule explicitly indicates who may perform
           the action Dependently;

      (c)  during the time between the announcement made under
           (a) of this Rule and the attempt to perform the action,

           (1)  fewer than N Players have publicly posted Objections
                to the performance of the action, with reasons, if the
                action is to be performed Without N Objections; or

           (2)  at least N Players other than the Player attempting to
                make the action have publicly posted Support for
                the performance of the action, if the action is to be
                performed With N Supporters; and

      (d)  e publicly announces that e performs the described action.

      Properties which would be changed by a Dependent Action
      do not occur until announced as in (d).

      The specification in the Rules that an action may be
      performed Dependently in no way prohibits performing that
      same action if doing so would otherwise be permissible.

      Rules calling for Dependent Actions may restrict the eligibility
      of Players to Support or Object to that specific Action.

