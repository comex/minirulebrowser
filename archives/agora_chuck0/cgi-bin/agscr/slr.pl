The Oligarchy
----------------------------------------------------------------------

Rule 1932/3 (Power=2)
The Oligarchy

      The Oligarchy is a set of six positions which are filled by
      Players, who are called Oligarchs. No Player may ever occupy
      more than one position in the Oligarchy at any one time. A
      position is vacant if and only if it is not occupied; hence
      there is a vacancy in the Oligarchy only when there are fewer
      than six Oligarchs.

      There are three ranks within the Oligarchy, termed High, Middle,
      and Low. There are at most one High Oligarch and at most two
      Middle Oligarchs. There may be as many as six Low Oligarchs, but
      the normal number is three.

      An Oligarch may resign eir position in the Oligarchy by
      publicly announcing e does so. Upon such an announcement, e
      ceases to be an Oligarch and eir position becomes vacant.

----------------------------------------------------------------------

Rule 1963/0 (Power=2)
Vacancies in the Oligarchy

      If an Oligarch is On Hold, is a Zombie, has three or more Blots,
      is the Speaker, or is rotated out of the Oligarchy, then the
      Grand Warden of the Oligarchy is required to remove that Player
      from the Oligarchy as soon as possible.

      The GWoTO removes a Player from the Oligarchy by correctly
      announcing that at least one of the above conditions has arisen
      for that Player. The named Player ceases to be an Oligarch as of
      the GWoTO's announcement, and eir position in the Oligarchy
      becomes vacant.

      An Oligarch who ceases to be a Player also ceases to be an
      Oligarch, and eir position in the Oligarchy becomes vacant.

----------------------------------------------------------------------

Rule 1964/0 (Power=1)
Filling Vacancies in the Oligarchy

      Whenever there exist Oligarchy positions which are neither
      occupied nor pending, and no Oligarchy Auction is in progress,
      the Grand Warden of the Oligarchy (GWoTO) shall initiate an
      Auction for a number of positions equal to the number of
      positions neither occupied nor pending. At most one Auction of
      Oligarchy positions shall be conducted at a time; if the number
      of vacant positions increases during an Auction, the GWoTO shall
      not initiate a new Auction until the current Auction ends.

----------------------------------------------------------------------

Rule 1936/6 (Power=2)
Auctioning Positions in the Oligarchy

      Whenever an Auction is conducted to fill a position or positions
      in the Oligarchy, the standard Auction procedure is used, with
      the following additions and exceptions:

      (a) Auction Currency: the Auction Currency is Stems.

      (b) Bidders: Only Politicians may bid. The Speaker, the
          Speaker-Elect, all Zombies, and all Oligarchs may not bid.

      (c) If a bidder ceases to be a Player, goes On Hold, or becomes
          a Zombie, an Oligarch, the Speaker or the Speaker-Elect
          during the Auction, then all eir bids in the Auction are
          cancelled.

      (d) Winning bids: when the Auctions ends, the winning bids are
          the N largest uncancelled bids made by different Players
          who are not Oligarchs, where N is the number of positions
          being Auctioned.

      (e) Defaulting: a winning bidder has one week from the time e
          is billed by the Auctioneer for eir winning bid in which to
          pay the bill. If the bill remains unpaid for more than one
          week, then the winning bidder is considered to have
          defaulted. In that case, the Auctioneer shall forgive the
          debt and penalise the defaulting bidder 2 Blots.

      A position in the Oligarchy is pending if the position is
      vacant, an Auction has been held for that position and a winning
      bidder determined, and the winning bidder has not defaulted.

      When a winning bidder pays the bill for eir winning bid in the
      Auction, e becomes a Low Oligarch, provided the position is
      pending.

----------------------------------------------------------------------

Rule 1937/6 (Power=2)
Distributing Positions in the Oligarchy

      When there are six Oligarchs and a number of Low Oligarchs other
      than three, the Oligarchy is &quot;abnormally distributed&quot;, and an
      Oligarchy selection takes place as set out below.

      After a Player has become an Oligarch, or an Oligarch has
      changed rank, then if the Oligarchy is abnormally distributed,
      the Grand Warden of the Oligarchy shall, within three days,
      publish a list of the Oligarchs and their ranks, identifying
      those Players who are required to make the selection, and the
      set of Players from whom the selection is to be made. Failure to
      do so is the Class 2 Infraction of Oligardiness, which may be
      reported by any Player. However, if the Oligarchy selection
      occurs before the GWoTO publishes the list, the GWoTO is
      relieved of eir obligation to publish it.

      Whenever there are six Oligarchs, and no High Oligarch, the Low
      Oligarchs are permitted to select a Middle Oligarch to become
      the new High Oligarch. If there are no Middle Oligarchs, the Low
      Oligarchs shall instead select one of their own number to become
      the new High Oligarch. The immediately prior High Oligarch may
      not be selected as the new High Oligarch, if e is presently a
      Low Oligarch.

      Whenever there are six Oligarchs, a High Oligarch, and fewer
      than two Middle Oligarchs, the High Oligarch is permitted to
      select a Low Oligarch to become Middle Oligarch.

      When a selection from a set of one candidate is to be made, that
      candidate is selected immediately without any action by any
      Player.

      When a selection from a set of two or more candidates is to be
      made, and that selection is not to be made by the Speaker, the
      individual or individuals who are to make the selection shall
      announce their recommendations in the Public Forum. They are
      permitted to make their recommendations from the time the
      Oligarchy becomes abnormally distributed. They are required to
      make their recommendations within three days of the GWoTO's
      announcement. Recommendations, once made, cannot be changed.

      The selection occurs when one candidate has received the
      recommendation of a majority of those persons required to make a
      recommendation. If three days pass from the GWoTO's announcement
      without one candidate receiving the recommendation of a
      majority, then if there is a candidate which has received more
      recommendations than any other candidate, that candidate is
      selected; otherwise, the decision passes to the Speaker, and the
      Speaker shall make the selection.

      When a selection from a set of two or more candidates is to be
      made, and that selection is required to be made by the Speaker,
      the Speaker shall make eir selection within seven days by
      announcing it in the Public Forum. A Speaker who fails to make
      such a selection becomes Tainted. If, at the time a Player
      becomes Speaker, the previous Speaker was required to make a
      selection by this Rule and had not yet done so, the new Speaker
      shall be instead required to make the selection, and shall do so
      within seven days of becoming Speaker, or become Tainted.

      If a vacancy in the Oligarchy arises when the Oligarchy is
      abnormally distributed, then the Oligarchy selection and all
      requirements on Players arising from it are cancelled.

      Any Player required to make a recommendation by this Rule who
      fails to do so within the allotted time commits the Class 2
      Infraction of Oligardiness; this Infraction may be reported by
      any Player.

----------------------------------------------------------------------

Rule 1938/2 (Power=2)
Rotation Out of the Oligarchy

      If the same Player has been High Oligarch continuously for at
      least four weeks, and there have been no vacancies in the
      Oligarchy during the past Nomic Week, then at the beginning of
      the next Nomic Week, the High Oligarch is rotated out of the
      Oligarchy.

