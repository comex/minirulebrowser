Rule 1958/0 (Power=1)
Nominating with Budgets

      If an Office has a Budget, then each Nominee for the Office
      shall submit a valid Proto-Budget during the Nomination Period,
      or else eir Nomination shall automatically be withdrawn at the
      end of the Nomination Period.  A Proto-Budget is valid if it
      would be valid as a Budget.

      When the Voting Period begins, the Vote Collector shall announce
      each Candidate's last valid Proto-Budget.

      When the Voting Period ends, the Winner's last valid
      Proto-Budget becomes the Budget of that Office, and e shall
      announce it as soon as possible.  If the Election ends without a
      Winner, then the existing Budget remains in effect.

