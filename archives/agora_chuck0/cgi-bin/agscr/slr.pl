Rule 1869/1 (Power=3)
Restrictions on Setting Power

      Except as described in this Rule, no entity can set the Power
      of another entity to exceed the Power of the entity causing
      the Power to be so set.  A Rule may set the Power of an adopted
      Proposal, even exceeding the Power of the Rule, so long as the
      Power of the adopted Proposal does not exceed the Voting Index
      of that Proposal.

      No entity may destroy or repeal an entity with Power greater
      than its own.

      No Rule may have Power less than 1 or greater than 4.

