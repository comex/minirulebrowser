Rule 1645/7 (Power=1)
Temporary Delegation of Office

      The Electee to an Office or the Speaker, if e holds that Office,
      is permitted to appoint another Player to perform the duties of
      that Office. This is called Delegation. The Player doing the
      appointing is herein referred to as the Delegating Player and
      the Player so appointed is herein referred to as the Delegated
      Player.

      An Office is legally Delegated if and only if the following
      conditions are met:

      (a) the Delegating Player has posted to the Public Forum a
          Notice of Delegation, naming the Office being Delegated and
          the Delegated Player;

      (b) the Delegated Player is permitted by the Rules to hold the
          Office at the commencement of the Period of Delegation;

      (c) the Delegated Player has posted, within the week immediately
          preceding or immediately following the posting of the Notice
          of Delegation, and to the Public Forum, a Notice of Consent
          to Delegation for that Office;

      (d) the Delegating Player is still permitted to Delegate the
          Office at the commencement of the Period of Delegation;

      (e) the Period of Delegation has commenced and not yet
          concluded.

      The Period of Delegation commences at the time the Notice of
      Delegation is posted, or at the time the Notice of Consent to
      Delegation is posted, or at the time (if any) specified by the
      Delegating Player, whichever is latest. The Period of Delegation
      concludes when any of the following occur:

      (a) a Player other than the Delegating Player becomes the
          Electee to the Office;
      (b) the time (if any) specified by the Delegating Player as the
          end of the Period of Delegation is reached; or
      (c) the Delegated Office has been continuously Delegated for
          three weeks.

      During the Period of Delegation, the Delegating Player ceases to
      hold the Delegated Office; the Delegated Player holds the
      Office, and earns the Salary for that Office as described in
      other Rules.

      At the end of the Period of Delegation, the Delegated Player
      ceases to hold the Delegated Office, and the Electee to the
      Office commences holding the Office, if the Office has an
      Electee. If the Office has no Electee, then the Delegated Player
      continues to hold the Office.

