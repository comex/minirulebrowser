Rule 889/10 (Power=1)
The Clerk of the Courts

      There exists the Office of Clerk of the Courts, whose
      responsibility it is to oversee the judicial system.

      The Monthly Report of the Clerk of the Courts shall include the
      Stare Decisis, which is a list of past Calls for Judgement
      (CFJs). The following information shall be included for each
      CFJ:

        (i)  its statement;
       (ii)  the date on which it was called;
      (iii)  its outcome (if any) on Judgement; and
       (iv)  its outcome (if any) on Appeal.

      Whenever a CFJ is made, the Clerk shall add it to the list.  E
      may, at eir discretion, add earlier CFJs.  E may, Without
      Objection, remove any CFJ from the list that e deems no longer
      relevant.

