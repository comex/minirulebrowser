Delivering Judgement
----------------------------------------------------------------------

Rule 408/14 (Power=1)
Late Judgement

      The Deliberation Period for a CFJ begins when the Clerk of the
      Courts announces the identity of the Judge, and ends seven days
      later.

      In the week following the end of the Deliberation Period, if the
      Judge has not yet returned Judgement or Dismissed the CFJ, then
      the Clerk of the Courts may recuse the Judge from the CFJ and
      assign it to a new Judge, by posting an announcement to that
      effect.

      A Judge who Judges or Dismisses a CFJ after the end of the
      Deliberation Period, but before being recused as a Judge,
      commits the Class 0.5 Infraction of Judging a Bit Late, to be
      detected and reported by the Clerk of the Courts.

      A Judge who has neither Judged nor Dismissed a CFJ seven days
      after the end of its Deliberation Period is automatically
      recused from that CFJ.

      A Judge who is recused from a CFJ in accordance with this Rule
      commits the Class 3 Infraction of Failure to Judge (to be
      detected and reported by the Clerk of the Courts), and becomes
      ineligible to be a Judge until e publicly requests to be made
      eligible again.

----------------------------------------------------------------------

Rule 591/16 (Power=1)
Legal Judgements

      A Judge judges a CFJ by sending eir Judgement to the Clerk of
      the Courts.  The Judgement of a CFJ must be either TRUE or
      FALSE.  Only the Judge assigned to a CFJ may Judge that CFJ.

      As soon as possible after the receipt of a legal Judgement, the
      Clerk shall distribute the Judgement to the Public Forum.

      If a Judge delivers Judgement on a CFJ before the end of
      the assigned deliberation period, then the Clerk of the
      Courts shall pay out to the Judge the Judicial Salary as
      soon as possible after the publication of eir Judgement.

----------------------------------------------------------------------

Rule 217/3 (Power=1)
Judgements Must Accord with the Rules

      All Judgements must be in accordance with the Rules; however,
      if the Rules are silent, inconsistent, or unclear on the
      Statement to be Judged, then the Judge shall consider game
      custom, commonsense, past Judgements, and the best interests of
      the game before applying other standards.

----------------------------------------------------------------------

Rule 1575/4 (Power=1)
Standards of Proof

      A CFJ alleging that a Player has violated a Rule or committed a
      Crime shall not be judged TRUE unless the evidence is sufficient
      to be certain of that Judgement beyond reasonable doubt.

      In all other CFJs, the Judgement shall be consistent with the
      preponderance of the evidence at hand.

      Furthermore, it is a defense to any accusation of a Crime that
      the Player reasonably believed that eir actions were not a
      Crime at the time e performed them, or that e reasonably did not
      know that e was required to perform an action, when such
      nonperformance is defined as a Crime.  A Player shall not be
      convicted of a Crime if this defense applies.

----------------------------------------------------------------------

Rule 1768/0 (Power=1)
Evidentiary Status of Officer Reports

      The published Report of an Officer constitutes prima facie
      evidence of the truth of those matters reported therein which
      that Officer is required by law to report.  This presumption may
      be set aside only by clear and convincing evidence to the
      contrary.

----------------------------------------------------------------------

Rule 451/3 (Power=1)
Determination of Judgement--Timing

      When a Judge is considering eir Judgement of a Statement
      contained in a CFJ, e shall make eir evaluation based on the
      truth or falsity of the Statement at the time the CFJ was
      issued.

----------------------------------------------------------------------

Rule 1565/8 (Power=1)
Dismissal of a CFJ

      A Judge must dismiss a CFJ if one of the following is true of
      it:

        i) It contains no clearly-identifiable Statement.
       ii) Its Statement can not logically admit to either being TRUE
           or FALSE.
      iii) Its Statement does not relate to a matter relevant to the
           Rules.
       iv) Its Statement fails to comply with the Rules.
        v) It lacks standing, as defined elsewhere.
       vi) After a reasonable effort by the Judge to obtain all
           relevant information, no determination can be made of the
           truth or falsity of its Statement.

      The Judge does this by notifying the CotC of the CFJ's
      dismissal, and the reasons e is doing so.  Dismissals are only
      legal when made for the reasons listed in this Rule.  Dismissal
      occurs when the CotC is notified of a legal dismissal.

      As soon as possible after being notified of a legal dismissal,
      the CotC must post to the Public Forum a notice of the dismissal
      and the Judge's reasons for doing so.

      If the dismissal, upon appeal, is subsequently set aside, then
      that CFJ is no longer dismissed. It shall be considered again
      and can not again be legally dismissed for the same reasons.

      A Judge who dismisses a CFJ before the end of the assigned
      deliberation period shall receive a Judicial Salary.

----------------------------------------------------------------------

Rule 502/7 (Power=1)
Salary for Judges

      The Clerk of the Courts shall, within one week of the time a
      Player earns a Judicial Salary, pay out that Judicial Salary to
      that Player.

      If a decision (or dismissal) which resulted in a Player earning
      a Judicial Salary is subsequently appealed and overturned on
      appeal, the Clerk of the Courts shall bill that Player for an
      amount equal to the Judicial Salary paid to that Player.

----------------------------------------------------------------------

Rule 1803/0 (Power=1)
Judicial Orders

      Judicial Orders are executed by being sent to the Clerk of the
      Courts, but do not take effect until their publication by the
      Clerk of the Courts.  The Clerk of the Courts shall publish each
      Judicial Order as soon as possible after receiving it from the
      Judge who executed it.

----------------------------------------------------------------------

Rule 1804/0 (Power=1)
Appeal of Judicial Orders

      Judicial Orders are subject to appeal to a Board of Appeal by a
      Call for Appeal.

      The Board of Appeal shall, upon finding that a Judicial Order is
      improperly or invalidly executed, order that that Order be
      amended or vacated, as it deems appropriate.

      When the Clerk of the Courts receives a properly executed Call
      for Appeal of a Judicial Order, the Clerk of the Courts shall
      stay the Order under appeal.  If the Board of Appeals affirms
      the validity of the original Order, it shall then vacate this
      stay.

----------------------------------------------------------------------

Rule 1826/0 (Power=1)
Motions

      A Motion is a formal request made by a Player to the Judge of a
      CFJ.

      A Motion is made by submitting it to the Clerk of the Courts,
      clearly identifying the CFJ to which the Motion applies.

      The Clerk of the Courts shall forward each properly-filed Motion
      to the Judge of the CFJ to which the Motion applies as soon as
      possible after receiving it.

----------------------------------------------------------------------

Rule 1827/2 (Power=1)
Granting or Denying Motions

      A Judge must either grant or deny each Motion forwarded to em by
      the Clerk of the Courts, within five days of when the Motion was
      received by the Judge.  E may, but need not, state the reasons
      for eir grant or denial.  A Judge grants or denies a Motion by
      sending eir determination on that Motion to the Clerk of the
      Courts, along with any reasons e chooses to provide.

      The effect of granting a Motion depends on the nature of the
      Motion granted, but generally amounts to requiring the Judge to
      perform as requested by the Motion.

      Upon receipt of a Judge's determination on a Motion, the Clerk
      shall note the determination made and the reasons, if any, on
      the record of the CFJ, and shall notify the Player who made the
      Motion of that determination.

      If a Judge fails to Grant or Deny a Motion within seven days
      of when it was forwarded to em by the Clerk of the Courts, e
      commits the Class 0.5 Infraction of Slow Motion, detected
      and reported by the Clerk of the Courts. If this occurs,
      the Clerk of the Courts shall Recuse the Judge and assign
      a new one as usual.

      If a Motion is made after a case is closed, and the original
      Judge of that case is no longer a Player or is On Hold, then
      a new Judge shall be assigned to the case as usual.

----------------------------------------------------------------------

Rule 1828/1 (Power=1)
Motions Requesting Judicial Orders

      Any Player may formally request a Judge issue any Judicial Order
      by filing a motion requesting that Order.  If granted, the Judge
      shall issue the Order requested.

----------------------------------------------------------------------

Rule 1509/3 (Power=1)
Orders to Compel

      Upon a judicial finding that a Player has failed to perform a
      duty required of em by the Rules, the Judge so finding shall
      execute an Order, requiring that Player to perform that duty as
      soon as possible.  Such an Order is known as an Order to
      Compel.

      If the duty in question arises because the Player in question
      holds a specific Office or other position of official
      responsibility, the Order shall be directed to that Office or
      position.  If the Player holding that Office fails to perform
      the duty in question as ordered, the Judge shall Order that that
      Player be removed from Office or position.

      If the duty in question arises for a reason not related to the
      Player in question holding a specific Office or official
      position, and that Player fails to perform the duty as ordered,
      the Judge shall amend the Order to require the Speaker to
      perform that duty instead.

----------------------------------------------------------------------

Rule 1829/1 (Power=1)
Refusal of Duty by the Speaker

      If the Speaker is convicted of the Crime of Contempt by
      Inaction, relating to a validly issued Order to Compel which was
      directed at the position of Speaker, the Speaker becomes
      Tainted.

----------------------------------------------------------------------

Rule 1830/0 (Power=1)
No Compulsion of Judges

      No valid Order to Compel may be directed to a Judge, or may
      require the performance of any duty required of Player by the
      virtue of that Player being a Judge.  Any such Order is invalid.
      Any CFJ alleging that a Judge has failed to perform a duty of a
      judicial nature shall be dismissed.

----------------------------------------------------------------------

Rule 789/5 (Power=1)
Orders to Annotate Rules

      The Judge of any CFJ, the Statement of which alleges that a Rule
      should be interpreted in a certain way, which is judged TRUE,
      may, at eir discretion, issue an Order requiring the Rulekeepor
      to annotate the Rule in question with the Statement of that CFJ.
      Such an annotation, while it exists, shall guide application of
      that Rule.

      The Rulekeepor may remove such an annotation only if that
      Rule is repealed; if required to do so by a valid Order;
      or if the original Order to annotate is amended, stayed, or
      vacated.  The Rulekeepor may vacate an Order to annotate
      a Rule Without Objection.  Annotations to a Rule may not be
      modified in any way except as specified in this Rule.

      If a Player believes that an annotation is no longer pertinent,
      e may file, in the original CFJ from which the Order of
      Annotation arises, a Motion to Vacate the Order of Annotation.
      If such a Motion is granted, the Judge granting it shall Order
      the original Order Vacated and shall Order the Rulekeepor to
      remove the annotation in question.

----------------------------------------------------------------------

Rule 1500/6 (Power=1)
Orders to Annotate SLCs

      The Judge of any CFJ, the Statement of which alleges that an SLC
      should be interpreted in a certain way, which is judged TRUE,
      may, at eir discretion, issue an Order requiring the Maintainer
      of that SLC to annotate the SLC in question with the Statement
      of that CFJ.  Such an annotation, while it exists, shall guide
      application of that SLC.

      The Maintainer of that SLC may remove such an annotation only
      if the SLC ceases to exist; if required to do so by a valid
      Order; or if the original Order to Annotate is amended, stayed,
      or vacated.  The Maintainer of an SLC may vacate an Order to
      annotate that SLC Without Objection.  Annotations to an SLC may
      not be modified in any way except as specified in this Rule.

      If a Player believes that an annotation is no longer pertinent,
      e may file, in the original CFJ from which the Order of
      Annotation arises, a Motion to Vacate the Order of Annotation.
      If such a Motion is granted, the Judge granting it shall Order
      the original Order Vacated and shall Order the Maintainer of
      that SLC to remove the annotation in question.

----------------------------------------------------------------------

Rule 1742/2 (Power=1)
Agreements between Players

      Players may make agreements among themselves with the intention
      that such agreements will be binding under the Rules. If such
      an agreement is subsequently broken, any Player party to that
      agreement may then call a CFJ alleging that the agreement has
      been broken. If the Judge of such a CFJ finds that the
      agreement was entered into with the intention that the
      agreement be binding under the Rules and that the agreement has
      in fact been broken, e may Order the breaching party to:
      (1) transfer Property to the other party or parties to remedy
          the damages from the breach,
      (2) perform according to the agreement, or
      (3) perform such other substitute acts as would fairly serve
          the interests of the agreement.
      E may further Order the other parties of the agreement to
      perform such acts as may be necessary to preserve fairness and
      justice.

      Nothing in this Rule shall be construed so as to impair the
      enforcement of an agreement which requires a Player to violate
      another agreement.

      A CFJ alleging that an agreement has been broken called by
      anyone who is not party to that agreement lacks standing and
      shall be dismissed.

----------------------------------------------------------------------

Rule 1365/6 (Power=1)
Concurring and Dissenting Opinions

      There shall exist a type of Application called an Application
      to Submit an Opinion.  Such an Application, if effective, has
      the effect of annotating a Judgement or Dismissal in a given
      Call for Judgement.  In order for such an Application to have
      effect upon Execution, it must satisfy the following
      requirements:

      * It must clearly indicate to which CFJ it applies.
      * It must be labeled as either a Concurring Opinion or
        a Dissenting Opinion.
      * It must indicate whether the Judgement or Dismissal to
        which it applies is that of the Judge or that of the
        Appeals Court.
      * It must be accompanied by reasons and arguments, which
        may include, but are not necessarily limited to, citations
        of deciding Rules, past Judgements, and Game Custom.
      * It must bear the Signatures of at least two Players.
      * It must be Executed no earlier than the submission of the
        Judgement or Dismissal to which it applies, and no later than
        one week after the publication by the Clerk of the Courts of
        that Judgement or Dismissal.

      An Application to Submit an Opinion is Executed by submitting
      it to the Clerk of the Courts.  Such an Application, having
      been Executed and having met all the requirements for
      effectiveness, is also called an Opinion.  It is also referred
      to as a Concurring Opinion or a Dissenting Opinion, as
      indicated in the Opinion.

      Once an Application to Submit an Opinion is Executed and takes
      effect, the Clerk of the Courts must distribute the Opinion to
      all Players as soon as possible.  Furthermore, the Opinion must
      be appended to the Legal Judgement or Dismissal.

