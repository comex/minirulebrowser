Rule 1957/1 (Power=1)
Budgets

      A Budget is a document maintained by a particular Office, whose
      holder is its Maintainer, and determining various values as
      explicitly required or permitted by the Rules.

      An Office has a Budget only if the Rules explicitly designate
      one.  Each Budget shall be part of the Monthly Reports of its
      Maintainer.  The Maintainer shall post the Budget to the Public
      Forum as soon as possible whenever it changes.

      If a current Budget is discovered to be invalid, then its
      Maintainer shall create a valid one as soon as possible.  This
      valid one should be as close as possible to the most recent
      valid version of the Budget.

      While holding an Office as Electee, an Officer may amend its
      Budget Without Objection; the Rules regarding a particular
      Budget may specify a different procedure for its amendment.

