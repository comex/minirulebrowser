Rule 1435/13 (Power=1)
Definition of Indulgences, Blots and Immaculate

      Indulgences are a Bank Currency. The MUQ of Indulgences is 0.1.
      The Recordkeepor for Indulgences is the Herald.

      A Blot is a stain on a Player's record, recorded by the Herald.

      Whenever the Rules state that a Player gains or is assessed
      some number of Blots, and specify a method of reporting that
      gain or assessment to the Herald, the Herald shall record this
      increase in the number of Blots staining the affected Player as
      soon as possible after receiving the notification.

      Blots are expunged from a Player's record when:
      (1) an entity transfers to the Bank some number of Indulgences,
          specifying in the Notice of Transfer that the transfer is
          for the purpose of expunging the Blots of some Player; a
          number of Blots equal to the number of Indulgences
          transferred are expunged from the Player specified, or all
          of that Player's Blots if more Indulgences are transferred
          than that Player has (and the excess indulgences returned
          to the transferor); or
      (2) the Rules otherwise establish some method by which Blots
          are to be expunged, provided that such method must include
          a requirement of notice to the Herald of the expungement
          and such notice is actually sent to the Herald.

      A player with zero Blots staining eir record is said to be
      Immaculate.

      Not being Immaculate is a Win-Preventing Condition.

----------------------------------------------------------------------

Rule 1962/1 (Power=1)
Win by Blots

      Being the only Immaculate Player is a Win Condition.  Upon the
      report of such a Win, the Herald shall expunge N Blots per
      Player, where N is the number of Blots on the
      Player(s) with the least nonzero number of Blots.

----------------------------------------------------------------------

Rule 1712/9 (Power=1)
Distribution of Indulgences

      The Ideal Indulgence Circulation Level (IICL) is the number of
      registered Players, plus the total number of Blots held by
      Players.

      The Actual Indulgence Circulation Level (AICL) is the total
      number of Indulgences owned by entities other than the Bank,
      augmented by the total number of Indulgences owned by the Bank
      which have been auctioned off to Winning Bidders in prior
      Indulgence Auctions but not yet paid for.

      As soon as possible after the start of each month, the Herald
      shall Auction a number of Indulgences equal to the difference
      between the AICL and the IICL. If the AICL is equal to or
      greater than the IICL, no auction shall be held. To calculate
      the difference, the Herald shall determine the number of Blots
      and the number of Indulgences owned as published in the most
      recent Herald's Report; other values in the formula shall be
      determined as of the time of the start of the auction.
      However, if the Herald makes an error in determining the
      number of Indulgences to be Auctioned, in good faith, that
      number shall be allowed to stand.

      The items to be auctioned are individual Indulgences, and thus
      the number of items is equal to the number of Indulgences to be
      Auctioned. The Auctioneer shall be the Herald, and the Auction
      shall be conducted in Stems. Only Acolytes may bid.

      Upon the satisfaction of a debt arising from an Indulgence
      Auction, the Herald shall pay out one Indulgence to the debtor
      of the satisfied debt.

----------------------------------------------------------------------

Rule 1714/7 (Power=1)
Lawlessness

      A Player is Lawless if more than 20 Blots stain eir record.

      Any Player may at any time publicly allege that another Player
      is Lawless.  If the allegation is incorrect, the Player who made
      the allegation commits the Class 15 Crime of Persecution.
      Otherwise, the following events occur in order:

        (i)  if the Lawless Player is the Speaker, the Speaker-Elect
             immediately becomes Speaker, and the Lawless Player
             ceases to be Speaker;

       (ii)  the Lawless Player is removed from any Offices e holds;
             and

      (iii)  the Lawless Player is deregistered.

      If a Player is deregistered according to the provisions of this
      Rule, e may not register again until a month has passed from eir
      deregistration.

      This Rule takes precedence over any Rule that would prohibit a
      Lawless Player from being deregistered.

----------------------------------------------------------------------

Rule 1747/3 (Power=1)
Pardoning Lawlessness

      The Clerk of the Courts has the power to Pardon Fugitives from
      Justice that have been deregistered for Lawlessness.  The Clerk
      may not exercise this power more than once per Nomic month, and
      may not Pardon the same person twice in a given term of Office
      (although, if re-elected later, e may again Pardon that person).
      The Clerk of the Courts Pardons a Fugitive from Justice by
      announcing e is doing so in the Public Forum. Then the following
      events shall occur:

         i) The Clerk of the Courts shall expunge as many of the Blots
            staining the Fugitive's record as required to reduce the
            number of Blots to one less than the threshold for
            Lawlessness and notify the Herald of this action.

        ii) After the above step has been completed, the Fugitive may
            reregister as a Player.

       iii) The Pardoned Player shall then post a Formal Apology to
            the Public Forum for eir Lawlessness. The CotC may include
            a list of up to ten Prescribed Words in eir Pardon.

        iv) For three months after being Pardoned, the Pardoned Player
            may only use Indulgences to expunge Blots from eir record
            and may not transfer any Indulgences for any other
            purpose.

      This Rule takes precedence over any Rule governing Lawlessness,
      or when a deregistered Player may reregister.

----------------------------------------------------------------------

Rule 1439/8 (Power=1)
Blots Due to a CFJ

      If a Call For Judgement (CFJ) clearly alleging that a Player has
      violated a specific Rule is found to be TRUE, the Player
      receives Blots equal to the Power of the violated Rule rounded
      down to the nearest whole integer, or four Blots if its
      Power exceeds four, unless the violated Rule explicitly defines
      or forbids a Blot penalty in the case of a CFJ.  The penalty
      shall be as defined in the Rule, if the Rule defines it; no Blot
      penalty shall be imposed if the Rule forbids it.

      The Player who initially called for the CFJ shall notify the
      Herald of the Blot assessment.

      If such a Judgement is overturned upon appeal, then the Player
      who received Blots shall expunge the Blots that were given due
      to the initial Judgement of the CFJ and notify the Herald of
      this action.

----------------------------------------------------------------------

Rule 1437/5 (Power=1)
Fugitives from Justice

      When a Player is deregistered from the game while not
      Immaculate, e becomes a Fugitive from Justice.  The Herald
      shall continue to keep a record of the number Blots staining
      each Fugitive from Justice.

      At the beginning of each month, the Herald shall halve the
      number of Blots staining each Fugitive of Justice, rounding
      down; but no Fugitive from Justice shall have eir number of
      Blots reduced to less than one by this method.

      When a Fugitive from Justice reregisters, e ceases to be a
      Fugitive from Justice.  The act of reregistration causes
      no change in eir number of Blots.

