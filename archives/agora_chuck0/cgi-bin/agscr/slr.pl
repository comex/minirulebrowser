Rule 1908/3 (Power=1)
Abandoned Property

      Property which is possessed by an entity which has no Executor,
      is deemed &quot;abandoned&quot;.

      The Notary shall be a Limited Executor of any entity which
      possesses abandoned Property, and on behalf of that entity e
      may:

      (1) transfer Property owned by that entity to any entity to
