Rule 1023/14 (Power=2)
Definition of &quot;As Soon As Possible&quot;

      Whenever a Player is required to perform an action &quot;as soon
      as possible&quot;, then e is required to perform the action within
      a week.

      A Player who fails to observe this requirement commits the
      Class 1 Infraction of Tardiness.  All Players are authorized to
      detect and report the commission of the Infraction of Tardiness.

      This Rule does not deprive actions which do not conform to its
      requirements of whatever effects they would otherwise have.
      Rather, this Rule defines the latest time at which actions to be
      performed &quot;as soon as possible&quot; may be performed without
      incurring a penalty.

      Other Rules may grant extensions of the penalty-free period to
      Inactive Players without conflicting with this Rule.

