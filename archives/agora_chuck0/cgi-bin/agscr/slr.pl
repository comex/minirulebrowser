Rule 1973/0 (Power=1)
Destruction of Currencies in Bond

      No Currency may be destroyed if there exist an unredeemed Bond
      whose face value is denominated in that Currency.

      This Rule takes precedence over any other Rule which permits the
      destruction of Currencies.

