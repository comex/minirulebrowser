          lost its Executor have been satisfied.

      If an entity which possesses property of any sort dies,
      dissolves, or otherwise ceases to exist, that entity is
      nonetheless considered to continue to exist for the purpose of
      those Rules pertaining to the possession and disposition of
      Property, for as long as it continues to possess Property.

