Rule 1599/6 (Power=1)
Overdue Debts

      The Executor of the creditor of any unsatisfied debt may, at
      any time, Order the debtor to satisfy that debt; but e may not
      do so if any other Order neither satisfied nor vacated exists
      requiring the same debtor to satisfy the same debt, or if the
      debt is disputed. The debtor shall have seven days to satisfy
      the debt from the time the Order is executed.

      Any entity Ordered to satisfy a debt who fails to do so within
      the time allowed by this Rule commits the Infraction of
      Persistent Indebtedness when that time first elapses, and
      further does so again every seven days after the time allowed
      has expired. Time during which the debt is disputed or the
      Order is stayed is not counted for the purpose of this Rule. If
      the Executor of the entity so Ordered is on Hold at the time
      the Order is issued, the time shall not begin to run until the
      Executor comes off Hold. These Infractions shall be reported by
      the creditor of the debt and carry a penalty of one Blot.

      No Order to satisfy a debt shall be enforced in any way after
      such time as the debt is satisfied, and the executor of any
      such Order shall vacate it as soon as the underlying debt is
      satisfied. If a debt becomes disputed, any Order to satisfy the
      disputed debt shall be stayed by its executor for the duration
      of the dispute. The Clerk of the Courts may stay, and a Judge
      stay or vacate, any Order which should properly be stayed or
      vacated (respectively) under this Rule in the event the
      executor of the Order neglects to do so.

