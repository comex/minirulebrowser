Rule 1884/4 (Power=1)
Zombie Masters

      Zombies are Property.  The owner of a Zombie is called eir
      Master.  Whenever a Player becomes a Zombie, eir Master is the
      Bank.

      Whenever the Master of a Zombie is a Player, e has Power of
      Attorney for that Zombie, unless the Rules withdraw it from
      em.

      The Registrar is the Recordkeepor of each Zombie.

----------------------------------------------------------------------

Rule 1885/3 (Power=1)
Zombie Auctions

      Whenever the Bank possesses a Zombie which is neither currently
      being Auctioned nor for which an Auction has been completed but
      the debt arising therefore not yet paid, the Registrar shall
      initiate an Auction for the sale of that Zombie.

      All Auctions of Bank-owned Zombies shall be conducted as
      follows:
      (1) The Auctioneer shall be the Registrar.
      (2) The Auction Currency shall be Indulgences.
      (3) The Starting Price shall be number of Blots of the Zombie
          being Auctioned; or, if the Zombie has no Blots, one
          Indulgence.

      As soon as possible after the end of a Zombie Auction, the
      Registrar shall expunge any Blots the Zombie has, notifying the
      Herald of this action.

      If there are no winning bids for a Zombie during a Zombie
      Auction, that Zombie is deregistered.

      The Registrar is a Limited Executor of the Bank, with the
      authority to satisfy debts of Zombies owed by the Bank to its
      creditors.

----------------------------------------------------------------------

Rule 1948/3 (Power=1)
Zombie Transfer Tax

      As soon as possible after a Zombie is transferred from a Player
      to another entity, the Registrar shall bill that Zombie's former
      Master prorated Maintenance on that Zombie.

      The amount of prorated Maintenance on a Zombie shall be equal
      to 0.1 Indulgence for each three days (or part thereof) elapsed
      between the time that Player most recently took possession
      of that Zombie, and the time that the transfer occurred, with a
      maximum of 1 Indulgence.

----------------------------------------------------------------------

Rule 1886/2 (Power=1)
Vivification Due to Noise

      If a Zombie becomes Noisy, eir Master ceases to own em, and
      e ceases to be a Zombie.

      Unless the Zombie was owned by the Bank when e became Noisy,
      then, as soon as possible, the Registrar shall pay out to the
      Zombie's most recent Master a number of Indulgences equal
      to the starting price in that Zombie's most recent Auction,
      halved for each new calendar month since the end of the Auction.

