Rule 698/5 (Power=1)
Always an Eligible Judge

      Every Active Player is eligible to Judge a given CFJ unless
      specifically made ineligible by some Rule.

      The Caller of a given CFJ is never eligible to Judge that CFJ.

      If, after taking all other rules affecting eligibility into
      account, there are no Players eligible to Judge a CFJ, then
      all Active Players, excluding the Caller and those Players
      Barred by the Caller, shall be eligible, any other Rule to
      the contrary notwithstanding.

      If this still does not result in there being any Players
      eligible to Judge, then all Players (Active or not), excluding
      the Caller and those Players Barred by the Caller, shall be
      eligible.

      If this still does not result in there being any Players
      eligible to Judge, then all Players, excluding the Caller, shall
      be eligible.

      This Rule takes precedence over any Rule or combination of Rules
      which would result in there being no Players eligible to Judge a
      given CFJ.

----------------------------------------------------------------------

Rule 1871/0 (Power=1)
Turns for All

      Whenever a Player is selected to judge a CFJ, e becomes
      ineligible to judge any future CFJs. If ever there are no
      Players eligible to Judge a CFJ, then all Players made
      ineligible by this rule become eligible again.

      Being made ineligible to judge a CFJ by this rule does not cause
      a Player to become ineligible to serve as a Justice on that CFJ.
      This rule takes precedence over any rule which claims otherwise.

----------------------------------------------------------------------

Rule 1567/9 (Power=1)
Making Oneself Ineligible to Judge a CFJ

      A Player makes emself ineligible to be the Judge of a specific
      CFJ or type of CFJ by transmitting a notice to the Clerk of the
      Courts, specifying the CFJ or type of CFJ for which e wishes to
      be made ineligible.  Such a notice remains in effect until the
      Player informs the Clerk of the Courts that it is no longer in
      effect.

      If a Player makes emself ineligible to be a Judge of a CFJ for
      which e has already been selected as Judge, e commits the Class
      0.1 Infraction of Shirking Judgeship, to be reported by the
      CotC.

----------------------------------------------------------------------

Rule 1568/5 (Power=1)
Judges On Hold

      Any Player who goes on Hold while selected as the Judge of one
      or more CFJs commits the Infraction of Judge Inactivity.  This
      Infraction is to be reported by the Clerk of the Courts, and has
      a penalty of 0.1 Indulgences times the number of CFJs for which
      the Player was selected as Judge when e went On Hold.

----------------------------------------------------------------------

Rule 1756/2 (Power=1)
Delinquent Judges

      The Clerk of the Courts shall recuse from a CFJ any Judge who is
      delinquent upon the demand of any two Players.  A Player demands
      the recusal of a delinquent Judge by posting eir demand in the
      Public Forum.

      A Judge recused for delinquency commits the Class 2 Infraction
      of Judicial Delinquency, to be reported by the CotC.

