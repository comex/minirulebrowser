Rule 459/3 (Power=1)
The Nomic Week

      The Nomic Week begins at the midnight, GMT, that begins each
      Monday. Any automatic change in the state of the Game which
      must occur weekly occurs at the beginning of the Nomic Week.
      Any automatic change which must occur monthly occurs at the
      midnight, GMT, which begins the first day of the month.  Any
      automatic change which must occur quarterly occurs at the
      midnight, GMT, which begins the first day of each calendar
      quarter.

      Other Rules may explicitly define alternate schedules for events
      or classes of events.  Any activity that must be performed at
      least weekly, monthly, or quarterly, must occur at least once in
      the given period.

