Rule 1377/14 (Power=1)
The Herald

      There exists the Office of Herald, whose responsibility it is to
      keep track of Blots and to be the Recordkeepor of Indulgences.

      The Herald's Report shall include the following:

        (i)  The number of Blots staining the records of each Player
