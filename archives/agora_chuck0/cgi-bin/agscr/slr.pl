Rule 1966/0 (Power=1)
Bonds

      Bonds are Property.  Each Bond, in addition to being a Property,
      has the following characteristics:
      (a) an issuer (which must be a Player or an entity with an
          Executor);
      (b) a face value, expressed as the positive multiple of the MUQ
          of some Currency; and
      (c) a maturity date.

      A species of Bond consists of all Bonds with the same issuer,
      face value and maturity date.  All Bonds of a given species are
      fungible.  In any transaction involving Bonds, it is sufficient
      to identify the Bonds involved by species.

      The Recordkeeper of Bonds shall be the Usuror.

      A Bond is mature on and after its maturity date, and is immature
      prior to that date.  If the maturity date of a Bond is specified
      only as a date, it shall be presumed to mature at midnight UTC
      of that day (the beginning of that day).

----------------------------------------------------------------------

Rule 1967/0 (Power=1)
Creation of Bonds

      New Bonds are created when an entity with Bond Issuance
      Authority sends a message to the Usuror stating that e is
      creating one or more new Bonds, provided that that message sets
      forth the face value and maturity date of each Bond to be
      created, and by paying the applicable Fee (if any) for creating
      those Bonds.  The newly created Bonds are possessed by the
      entity sending the message.  The issuer of the newly created
      Bonds is the entity sending the message.

      The Fee for creating Bonds shall be equal to the Basic Fee for
      issuing Bonds.  However, the Fee shall only be required for the
      first Bond of a given species created in the same message; there
      is no additional fee for additional Bonds of the same species
      created in the same message.  Furthermore, the Bank need never
      pay a Fee to create Bonds.

      This Rule defers to any Rule which imposes further restrictions
      on the ability of an entity to create new Bonds.

      The Bank, each Player, each Contest, and each Group all have
      Bond Issuance Authority.

----------------------------------------------------------------------

Rule 1968/0 (Power=1)
Destruction of Bonds

      Any entity which possesses a Bond may destroy that Bond by
      sending a message to the Usuror identifying the Bonds to be
      destroyed.

----------------------------------------------------------------------

Rule 1969/0 (Power=1)
Redemption of Bonds

      Any entity which possesses an unredeemed, mature Bond may redeem
      it by transferring the Bond to its Issuer, expressing in eir
      Notice of Transfer the purpose of redeeming the Bond.

      When the issuer of a Bond receives an unredeemed, mature Bond
      from another entity where the transfer was executed for the
      purpose of redemption, the Bond in question is thereby redeemed,
      and the issuer incurs a debt to the transferor of that Bond for
      the face value of the bond.

      If the issuer of a Bond receives an unredeemed, immature Bond
      from another entity where the transfer was executed for the
      purpose of redemption, the issuer incurs a debt to the
      transferor for that Bond; but if the Bond in question matures
      while still in the possession of the issuer, this debt is
      cancelled and the issuer incurs a debt to the transferor of that
      Bond for the face value of the Bond, as if the transfer had been
      made at the time the Bond matured.

      Once redeemed, a Bond may not be further transferred.

----------------------------------------------------------------------

Rule 1971/0 (Power=1)
Bank Issuance of Bonds

      The Bank, by the Treasuror, may issue Bonds with two Supporters.
      The number and species of Bonds to be issued must be set forth
      at the time the Treasuror seeks Support for the action.  The
      Bank may not otherwise issue Bonds except through the mechanism
      set forth in this paragraph.

      The Bank, by the Treasuror, may Auction Bonds Without Three
      Objections.  Each Auction of Bank Bonds shall consist of some
      number of a single species of Bond.  The species of Bond, the
      number of Bonds of that species, the Auction Currency, and the
      Minimum Bid of each Auction shall be determined by the Treasuror
      as part of the message indicating eir intent to hold a Bank Bond
      Auction.  The Auctioneer of all Bank Bond Auctions shall be the
      Treasuror.

      The Bank is not required to actually possess the Bonds it
      intends to Auction at the time the intent to conduct an Auction
      is announced or at the time the Auction begins.  However, if the
      Bank, by virtue of not having sufficient quantity of some
      species of Bond, is unable to satisfy a debt arising from a Bond
      Auction within seven days of when the Auction ends, the Player
      who was Treasuror at the time the Auction began commits the
      offense of Selling the Blue Sky, a Class 6 Crime.

----------------------------------------------------------------------

Rule 1972/0 (Power=1)
Acceleration of Bonds

      Whenever an entity which is the issuer of one or more
      nonredeemed Bonds ceases to have an Executor, all unredeemed
      Bonds issued by that entity are accelerated as described by this
      Rule.

      When a Bond is accelerated, the Bond immediately becomes mature
      and is deemed to have been redeemed.  The issuer becomes liable
      for a debt to the current owner of the Bond for the face value
      of the Bond.

----------------------------------------------------------------------

Rule 1977/0 (Power=1)
Selling Bonds to the Bank

      A Player may sell a Bond by paying a Fee equal to that Bond,
      and informing the Usuror that e is selling the Bond.

      If the rules permit this sale, then Usuror shall then pay out to
      that player an amount equal to the face value of the Bond
      multiplied by the applicable purchase rate set in the Usuror's
      Budget. The value of the purchase rate shall be determined as at
      the time the Bond was transferred.

      If the rules do not permit the sale, then the Bank incurs a debt
      to the transferor for that Bond.

      The Bank, by the Treasuror, may redeem any mature Bonds it owns.

----------------------------------------------------------------------

Rule 1978/0 (Power=1)
Credit and Credit Limits

      A Player's current credit is equal to the sum of the values of
      all Bonds owned by the Bank issued by that Player with a face
      value in Stems. Each Player's credit value shall be part of the
      Usuror's Weekly Report.

      The sale of a Bond is permitted only if it would not cause that
      Player's credit to exceed the Credit Limit set forth in the
      Usuror's Budget.

----------------------------------------------------------------------

Rule 1979/0 (Power=1)
Treasuror's Moratorium on Bond Purchases

      The Treasuror may place or remove a moratorium on all bond
      purchases with Support. Once a moratorium has been placed the
      Bank may not purchase any Bonds until the veto is removed.

      If a moratorium is in place, the Treasuror must report this in
      eir Monthly Report.

