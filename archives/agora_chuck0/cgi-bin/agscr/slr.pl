Rule 1503/4 (Power=1)
Crimes and Infractions

      Any entity who performs an action defined by the Rules to be a
      Crime, or fails to perform an action where such failure is
      defined by the Rules to be a Crime, shall be subject to whatever
      penalty the Rules prescribe for that Crime upon the execution of
      a Sentencing Order executed consequent to a judicial finding
      that e did in fact commit that Crime.

----------------------------------------------------------------------

Rule 1815/0 (Power=1)
Crimes and Infractions without Penalties

      No action is a Crime or an Infraction unless defined as such by
      the Rules.  If the Rules define an act to be a Crime or an
      Infraction, but fail to prescribe a penalty for that act, then
      there is no penalty for that act.  If the Rules define an act to
      be an Infraction, but fail to authorize any Player or Players to
      report instances of that Infraction, then that Infraction cannot
      be reported.

----------------------------------------------------------------------

Rule 1504/6 (Power=1)
Sentencing Orders

      The imposition of penalties for the commission of a Crime shall
      be by Sentencing Order(s).  Upon a judicial finding that an
      entity has committed a Crime, the Judge so finding shall execute
      Sentencing Orders sufficient to implement the penalty required
      by the Rules for that Crime.

----------------------------------------------------------------------

Rule 1812/2 (Power=1)
Notices of Infraction

      If an entity engages in any action or non-action which is
      defined by the Rules as an Infraction, then the Player
      authorized to report said Infraction may execute a Notice
      of Infraction by posting to the Public Forum that it is eir
      informed belief that the entity has committed an Infraction.

      A Notice of Infraction shall include the specific Infraction
      which has been committed, the entity which has committed it,
      the Rule(s) defining said Infraction, and sufficient details
      regarding the circumstances surrounding the offending action
      (or inaction) to identify the offense.

      A Notice of Infraction shall also include any and all
      necessary Orders or other actions to impose the mandated
      penalty for the Infraction which has been committed.

----------------------------------------------------------------------

Rule 1814/1 (Power=1)
Timing of Penalties for Crimes and Infractions

      The punishment for a Crime or Infraction shall be whatever was
      specified by the Rules at the time the action so designated as a
      Crime or an Infraction was committed, even if the Rule or Rules
      which specified the punishment, or which designated the action
      as a Crime or Infraction, have since been amended or repealed.
      This Rule takes precedence over any Rule which specifies
      a different penalty for a Crime or Infraction.

----------------------------------------------------------------------

Rule 1816/0 (Power=1)
No Double Jeopardy for Crimes

      Any CFJ whose Statement alleges that an entity has, through
      action or inaction, committed a Crime shall be dismissed if
      there is a prior CFJ, the statement of which alleges that the
      same entity has, through the same action or inaction, committed
      the same Crime, and which was itself not dismissed.

----------------------------------------------------------------------

Rule 1505/7 (Power=1)
Standard Classes of Crimes and Infractions

      A Class N Crime or Infraction, where N is replaced with a
      number, is a Crime or Infraction for which the penalty is
      N Blots.

