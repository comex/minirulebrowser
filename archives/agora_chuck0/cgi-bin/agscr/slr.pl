Rule 1942/1 (Power=1)
Property

      Any entity which the Rules permit to be possessed by another
      entity is a Property. Within the context of the Rules, the term
      &quot;possess&quot; refers to the legal status of possessing a Property;
      the terms &quot;possessor&quot; and &quot;owner&quot; are synonymous, as are
      &quot;possess&quot; and &quot;own&quot;.  Individual Properties may not be created
      or destroyed, or their ownership changed, except in accordance
      with the Rules.

      Every Property shall have a Recordkeepor, which is some Player
      who is required to retain a record of who owns that Property.
      If, for a given Property, no other Rule provides otherwise, or
      if the Rules would otherwise provide for a person who is not a
      Player, the Treasuror shall maintain this record. If one of the
      Official duties of an Officer is to be the Recordkeepor of a
      Property, then eir Report shall include this record and any
      changes thereto since the last posting of eir Report.

