Rule 1934/3 (Power=2)
The Speaker, Sanity, and Buying Democracy

        An Untainted Speaker may declare an Interested Proposal, which
        would otherwise be an Ordinary Proposal, for which the
        Voting Period has not yet concluded, and which has never been
        Contested, a Sane Proposal, With 3 Supporters.  This
        declaration may occur before the Voting Period commences; if
        it does, then this supported declaration turns the Proposal
        into a Democratic Proposal, and makes it Sane, requiring its
        Distribution Cost to be modified as required by other Rules.
        If this supported declaration occurs during the Voting Period
        for the Proposal, then the Proposal is not modified unless at
        least one Papyrus is transferred to the Bank for this stated
        purpose by any Player.  If this transfer occurs, then the
        Proposal becomes Democratic and Sane, all Votes already cast
        on the Proposal prior to the change are cancelled and its
        Voting Period is extended to end seven days after it became a
        Sane Proposal.

        Any Player may Contest an Interested Proposal, which would
        otherwise be an Ordinary Proposal and for which the Voting
        Period has not yet concluded, by sending a message to the
        Public Forum stating e is doing so.  If this Player is an
        Untainted Speaker, then the Proposal becomes Contested when e
        receives three Supporters.  If this Player is not an Untainted
        Speaker, then the Proposal becomes Contested if and only if e
        receives three Supporters and e publicly transfers 0.1VE to
        the Bank for the Contest.

        If a Proposal is Contested, any Player may publicly transfer
        any number of VEs to the Bank with a clear. unambiguous
        statement that this transfer is made either for or against the
        Contest.  At the end of the Voting Period, the Contest is
        Succesful if strictly more VEs have been publicly transferred
        to the Bank for the Contest than have been publicly
        transferred to the Bank against the Contest.  If equal numbers
        of VEs have been publicly transferred to the Bank both for and
        against the Contest, then the Contest is Succesful only if the
        Proposal was Contested by an Untainted Speaker.

        If the Contest is not Succesful, the Voting Period is
        considered to have ended, and the Proposal is considered an
        ordinary Ordinary Proposal for any further actions.

        If the Contest is Succesful, then all Votes cast on the
        Proposal are cancelled.  The Proposal becomes a Democratic
        Proposal.  The Voting Period is considered not to have ended,
        and is extended a further seven days.

        The Assessor shall be required to determine whether or not the
        Contest is Succesful, and shall report any results of this
        determination as soon as possible.

