Rule 1950/1 (Power=2)
Voting Power

      An Entity's Voting Power on an Ordinary Proposal is as follows:

         (a) The High Oligarch: three;
         (b) A Middle Oligarch: two;
         (c) A Low Oligarch: one;
         (d) Any other entity: zero.

      An Entity's Voting Power on a Democratic Proposal is as follows:

         (a) A Player: one plus the number of Voting Entitlements
             e possesses rounded down, minus one for every five Blots
             e has, with a minimum of zero and a maximum of:
             (1) for the High Oligarch, one;
             (2) for a Middle Oligarch, two;
             (3) for a Low Oligarch, three;
             (4) for a non-Oligarch, five.
         (b) The Bank: zero;
         (c) Any other entity: as defined in the Rules, with a default
             of zero if the Rules don't specify the Voting power on a
             Democratic Proposal for that entity.

      At the beginning of a week, the Assessor shall determine each
      entity's Voting Power on Democratic and Ordinary Proposals.
      These values shall then be in effect for all Proposals of the
      proper type distributed during that week. However, if a Proposal
      is made Democratic after it has been distributed, the Voting
      Power of each entity for that Proposal is redetermined at the
      time the Proposal is made Democratic.

      An entity may cast as many votes as e wishes on a Proposal, up
      to the limit determined by that entity's Voting Power on
      that Proposal, with the exception that no Player may vote on an
      Ordinary Proposal unless e is an Oligarch at the time e casts
      eir vote. An entity may cast its votes in any combination e
      wishes.

