Rule 1042/13 (Power=1)
Zombification Due to Silence

      Each Player is either Noisy, Quiet or Silent. Whenever a Player
      posts to the Public Forum, or a new Player registers, that
      Player is Noisy.

      Any Player who has not caused another Player to become Quiet
      within the last 24 hours can cause another Active Noisy Player
      to become Quiet by stating that e is doing so, and clearly
      identifying that Player, in the Public Forum.  If a Player has
      been Quiet continuously for two weeks, e becomes Silent.  If a
      Player has been Inactive continuously for two months, e becomes
      Silent.

      Any Player can cause a Silent Player to become a Zombie by
      stating in the Public Forum that the Silent Player has
      Abandoned the game. If the Speaker is Silent, and it is alleged
      in the Public Forum that e has Abandoned the Game, e commits
      the Class 15 Crime of Speaker Abandonment. E ceases to be
      Speaker, and the Speaker-Elect becomes Speaker. E then becomes
      a Zombie. In either case, the Registrar shall, as soon as
      possible after an allegation in the Public Forum that a Player
      has Abandoned the game, confirm or deny in the Public Forum
      that the Player has become a Zombie.

      The Registrar shall include in eir Report the most recent
      date on which each Player changed from being Noisy, Quiet or
      Silent to any of the others.

